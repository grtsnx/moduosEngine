#!/usr/bin/env node

import { randomBytes } from 'node:crypto';
import { spawnSync } from 'node:child_process';
import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';

const ROOT = process.cwd();
const ENV_PATH = join(ROOT, '.env');

function parseArgs(argv) {
  const options = {
    email: undefined,
    password: undefined,
    name: undefined,
    dryRun: false,
  };

  for (const arg of argv) {
    if (arg === '--dry-run') {
      options.dryRun = true;
      continue;
    }

    const match = arg.match(/^--(email|password|name)=(.*)$/);
    if (match) {
      options[match[1]] = match[2];
    }
  }

  return options;
}

function loadEnvFile(path) {
  if (!existsSync(path)) {
    return {};
  }

  const values = {};
  const content = readFileSync(path, 'utf8');

  for (const line of content.split('\n')) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) {
      continue;
    }

    const separator = trimmed.indexOf('=');
    if (separator === -1) {
      continue;
    }

    const key = trimmed.slice(0, separator).trim();
    let value = trimmed.slice(separator + 1).trim();

    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }

    values[key] = value;
  }

  return values;
}

function formatEnvValue(value) {
  if (/[\s#"'\\]/.test(value)) {
    return `"${value.replace(/\\/g, '\\\\').replace(/"/g, '\\"')}"`;
  }

  return value;
}

function upsertEnvVars(path, entries) {
  const original = existsSync(path) ? readFileSync(path, 'utf8') : '';
  let content = original;
  const missing = [];

  for (const [key, value] of Object.entries(entries)) {
    const line = `${key}=${formatEnvValue(value)}`;
    const regex = new RegExp(`^${key}=.*$`, 'm');

    if (regex.test(content)) {
      content = content.replace(regex, line);
    } else {
      missing.push(line);
    }
  }

  if (missing.length > 0) {
    const prefix =
      content.length > 0 && !content.endsWith('\n') ? '\n' : content.length > 0 ? '' : '';

    const block = [
      '',
      '# Backoffice superadmin seed (generated by scripts/seed-superadmin.mjs)',
      ...missing,
      '',
    ].join('\n');

    content = `${content}${prefix}${block}`;
  }

  writeFileSync(path, content, 'utf8');
}

function resolvePlatformSlug(env) {
  const platformName = env.PLATFORM_NAME?.trim() || 'penielvault';
  return platformName.toLowerCase().replace(/[^a-z0-9]+/g, '');
}

function generatePassword() {
  return randomBytes(18).toString('base64url');
}

function main() {
  const cli = parseArgs(process.argv.slice(2));
  const env = loadEnvFile(ENV_PATH);

  const email =
    cli.email?.trim() ||
    env.BACKOFFICE_ADMIN_EMAIL?.trim() ||
    `superadmin@${resolvePlatformSlug(env)}.local`;

  const password =
    cli.password?.trim() ||
    env.BACKOFFICE_ADMIN_PASSWORD?.trim() ||
    generatePassword();

  const name =
    cli.name?.trim() ||
    env.BACKOFFICE_ADMIN_NAME?.trim() ||
    'Superadmin';

  const seedVars = {
    BACKOFFICE_ADMIN_EMAIL: email,
    BACKOFFICE_ADMIN_PASSWORD: password,
    BACKOFFICE_ADMIN_NAME: name,
  };

  if (!env.DATABASE_URL?.trim()) {
    console.error(
      '[seed-superadmin] DATABASE_URL must be set in .env before seeding.',
    );
    process.exit(1);
  }

  console.log('[seed-superadmin] Superadmin credentials:');
  console.log(`  email:    ${email}`);
  console.log(`  password: ${password}`);
  console.log(`  name:     ${name}`);
  console.log('  role:     admin');

  if (cli.dryRun) {
    console.log('[seed-superadmin] Dry run: skipped writing .env and seeding.');
    return;
  }

  upsertEnvVars(ENV_PATH, seedVars);
  console.log(`[seed-superadmin] Updated ${ENV_PATH}`);

  const result = spawnSync('bun', ['prisma/seed.ts'], {
    cwd: ROOT,
    stdio: 'inherit',
    env: {
      ...process.env,
      ...seedVars,
    },
  });

  if (result.status !== 0) {
    process.exit(result.status ?? 1);
  }

  console.log('[seed-superadmin] Done. Sign in with the credentials above.');
}

main();
